import streamlit as st

# عنوان التطبيق
st.title("نموذج فحص السرطان")

# إدخال بيانات المريض
age = st.number_input("العمر:", min_value=0, max_value=120)
gender = st.selectbox("الجنس:", ["ذكر", "أنثى"])
symptoms = st.multiselect("الأعراض:", ["فقدان الوزن", "تعب شديد", "ألم", "كحة مزمنة", "تعرق ليلي"])

# زر الفحص
if st.button("فحص السرطان"):
    # هنا يمكنك إضافة نموذج لتحديد ما إذا كان هناك سرطان أم لا بناءً على البيانات المدخلة
    # هذا مثال بسيط يعتمد على الأعراض والعمر:
    if age > 50 and ("فقدان الوزن" in symptoms or "ألم" in symptoms):
        result = "قد تكون لديك مخاطر أعلى للإصابة بالسرطان. يُنصح بمراجعة الطبيب."
    else:
        result = "لا توجد علامات واضحة تشير إلى الإصابة بالسرطان، لكن يجب متابعة الأعراض."

    st.success(result)

# إضافة ملاحظات
st.info("يرجى ملاحظة أن هذا التطبيق ليس بديلاً عن استشارة طبية.")
